class Preloader extends CustomPainter {
  @override
  void paint(Canvas canvas, Size size) {
    Paint paint0Fill = Paint()..style = PaintingStyle.fill;
    paint0Fill.color = Color(0xff773DF3).withOpacity(0.16);
    canvas.drawCircle(Offset(size.width * 0.2500000, size.height * 0.2500000),
        size.width * 0.1666667, paint0Fill);

    Paint paint1Fill = Paint()..style = PaintingStyle.fill;
    paint1Fill.color = Color(0xff773DF3).withOpacity(0.54);
    canvas.drawCircle(Offset(size.width * 0.7500000, size.height * 0.7500000),
        size.width * 0.1666667, paint1Fill);

    Paint paint2Fill = Paint()..style = PaintingStyle.fill;
    paint2Fill.color = Color(0xff773DF3).withOpacity(1.0);
    canvas.drawOval(
        Rect.fromCenter(
            center: Offset(size.width * 0.2500000, size.height * 0.7500000),
            width: size.width * 0.3333333,
            height: size.height * 0.3333333),
        paint2Fill);

    Paint paint3Fill = Paint()..style = PaintingStyle.fill;
    paint3Fill.color = Color(0xff773DF3).withOpacity(0.38);
    canvas.drawOval(
        Rect.fromCenter(
            center: Offset(size.width * 0.7500000, size.height * 0.2500000),
            width: size.width * 0.3333333,
            height: size.height * 0.3333333),
        paint3Fill);
